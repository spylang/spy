@UNSAFE.builtin_func(color="blue", kind="metafunc")
def w_ptr_cmp(
    vm: "SPyVM", wam_a: W_MetaArg, wam_b: W_MetaArg, wam_n: W_MetaArg
) -> W_OpSpec:
    w_a_T, w_b_T = _check_ptrs_match(vm, wam_a, wam_b)
    A = Annotated[W_Ptr, w_a_T]
    B_ = Annotated[W_Ptr, w_b_T]
    ITEMSIZE = sizeof(w_a_T.w_itemT)
    ns = UNSAFE.w_ptr_cmp.compute_inner_ns([w_a_T, w_b_T])
    irtag = IRTag("unsafe.memop", cfunc="spy_ptr_cmp")

    @vm.register_builtin_func(ns, "impl", irtag=irtag)
    def w_ptr_cmp_impl(vm: "SPyVM", w_a: A, w_b: B_, w_n: W_I32) -> W_I32:
        n = vm.unwrap_i32(w_n)
        if n > w_a.length or n > w_b.length:
            raise SPyError("W_PanicError", "ptr_cmp out of bounds")
        result = vm.ll.call("_spy_memcmp", w_a.addr, w_b.addr, n * ITEMSIZE)
        return vm.wrap(result)

    return W_OpSpec(w_ptr_cmp_impl, [wam_a, wam_b, wam_n])


@UNSAFE.builtin_func(color="blue")
def w_mem_read(vm: "SPyVM", w_T: W_Type) -> W_Dynamic:
    T = Annotated[W_Object, w_T]
    ns = UNSAFE.w_mem_read.compute_inner_ns([w_T])

    @vm.register_builtin_func(ns, "impl")
    def w_mem_read_T(vm: "SPyVM", w_addr: W_I32) -> T:
        addr = vm.unwrap_i32(w_addr)
        return generic_mem_read(vm, addr, w_T)

    return w_mem_read_T


@UNSAFE.builtin_func(color="blue")
def w_mem_write(vm: "SPyVM", w_T: W_Type) -> W_Dynamic:
    T = Annotated[W_Object, w_T]
    ns = UNSAFE.w_mem_write.compute_inner_ns([w_T])

    @vm.register_builtin_func(ns, "impl")
    def w_mem_write_T(vm: "SPyVM", w_addr: W_I32, w_val: T) -> None:
        addr = vm.unwrap_i32(w_addr)
        generic_mem_write(vm, addr, w_T, w_val)

    return w_mem_write_T


def generic_mem_read(vm: "SPyVM", addr: int, w_T: W_Type) -> W_Object:
    from spy.vm.modules.posix import POSIX, W__FILE

    if w_T is B.w_i32:
        return vm.wrap(vm.ll.mem.read_i32(addr))
    elif w_T is B.w_u8:
        return W_U8(vm.ll.mem.read_u8(addr))
    elif w_T is B.w_f64:
        return vm.wrap(vm.ll.mem.read_f64(addr))
    elif w_T is B.w_str:
        v_addr, v_length = vm.ll.mem.read_ptr(addr)
        assert v_length == 1
        return W_Str.from_ptr(vm, v_addr)
    elif w_T is POSIX.w__FILE:
        return W__FILE(vm.ll.mem.read_i32(addr))
    elif isinstance(w_T, W_PtrType):
        v_addr, v_length = vm.ll.mem.read_ptr(addr)
        return W_Ptr(w_T, v_addr, v_length)
    elif isinstance(w_T, W_StructType):
        # read the struct by value, field by field
        values_w = {}
        for w_field in w_T.iterfields_w():
            fname = w_field.name
            offset = w_field.offset
            values_w[fname] = generic_mem_read(vm, addr + offset, w_field.w_T)
        return W_Struct(w_T, values_w)
    else:
        t = w_T.fqn.human_name(vm)
        raise WIP(f"Cannot read memory of type `{t}`")


def generic_mem_write(vm: "SPyVM", addr: int, w_T: W_Type, w_val: W_Object) -> None:
    from spy.vm.modules.posix import POSIX, W__FILE

    if w_T is B.w_i32:
        v = vm.unwrap_i32(w_val)
        vm.ll.mem.write_i32(addr, v)
    elif w_T is B.w_u8:
        assert isinstance(w_val, W_U8)
        vm.ll.mem.write_u8(addr, int(w_val.value))
    elif w_T is B.w_f64:
        v = vm.unwrap_f64(w_val)
        vm.ll.mem.write_f64(addr, v)
    elif w_T is B.w_str:
        assert isinstance(w_val, W_Str)
        v = w_val.ptr
        assert 0 < v < 2**31 - 1
        vm.ll.mem.write_ptr(addr, v, 1)
    elif w_T is POSIX.w__FILE:
        assert isinstance(w_val, W__FILE)
        v = w_val.h
        vm.ll.mem.write_i32(addr, v)
    elif isinstance(w_T, W_PtrType):
        assert isinstance(w_val, W_Ptr)
        vm.ll.mem.write_ptr(addr, w_val.addr, w_val.length)
    elif isinstance(w_T, W_StructType):
        assert isinstance(w_val, W_Struct)
        # write the struct by value, field by field
        for w_field in w_T.iterfields_w():
            fname = w_field.name
            offset = w_field.offset
            generic_mem_write(vm, addr + offset, w_field.w_T, w_val.values_w[fname])
    else:
        t = w_T.fqn.human_name(vm)
        raise WIP(f"Cannot write memory of type `{t}`")
